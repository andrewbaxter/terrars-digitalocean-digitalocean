pub struct BuildDataDatabaseConnectionPool {
    pub tf_id: String,
    #[doc= ""]
    pub cluster_id: PrimField<String>,
    #[doc= ""]
    pub name: PrimField<String>,
}

impl BuildDataDatabaseConnectionPool {
    pub fn build(self, stack: &mut Stack) -> DataDatabaseConnectionPool {
        let out = DataDatabaseConnectionPool(Rc::new(DataDatabaseConnectionPool_ {
            shared: stack.shared.clone(),
            tf_id: self.tf_id,
            data: RefCell::new(DataDatabaseConnectionPoolData {
                depends_on: core::default::Default::default(),
                provider: None,
                for_each: None,
                cluster_id: self.cluster_id,
                id: core::default::Default::default(),
                name: self.name,
            }),
        }));
        stack.add_datasource(out.0.clone());
        out
    }
}

